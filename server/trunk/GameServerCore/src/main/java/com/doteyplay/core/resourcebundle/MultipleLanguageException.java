package com.doteyplay.core.resourcebundle;

/**
 * 
 * @author 
 *
 */
public class MultipleLanguageException extends Exception {

    /**
	 * 
	 */
	private static final long serialVersionUID = -6771931440920865568L;

	int reqIndex_;
    int pSize_;

    public MultipleLanguageException(int requestedIndex, int paramSize) {
        reqIndex_ = requestedIndex;
        pSize_ = paramSize;
    }

    public int getRequestedIndex() {
        return reqIndex_;
    }

    public int getParamSize() {
        return pSize_;
    }
}
