package com.doteyplay.core.bhns.source.options;

public class ServiceEndpointInfo
{
	private byte endpointId;
	private String endpointIP;
	private int endpointPort;

	public ServiceEndpointInfo(byte endpointId, String endpointIP, int endpointPort)
	{
		super();
		this.endpointId = endpointId;
		this.endpointIP = endpointIP;
		this.endpointPort = endpointPort;
	}

	public byte getEndpointId()
	{
		return endpointId;
	}

	public void setEndpointId(byte endpointId)
	{
		this.endpointId = endpointId;
	}

	public String getEndpointIP()
	{
		return endpointIP;
	}

	public void setEndpointIP(String endpointIP)
	{
		this.endpointIP = endpointIP;
	}

	public int getEndpointPort()
	{
		return endpointPort;
	}

	public void setEndpointPort(int endpointPort)
	{
		this.endpointPort = endpointPort;
	}

}
