package com.doteyplay.game.constants.skill;

/**
 * 效果类型
 */
public enum SkillEffectType
{

	/**
	 * 攻击
	 */
	EFFECT_TYPE_COMMON_ATK()
	{
		@Override
		public String toString()
		{
			return "攻击";
		}
	},
	;

	public SkillEffectType get(int order)
	{
		return values()[order];
	}
	
}
