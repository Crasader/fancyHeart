package com.doteyplay.simplescheme;

public class SimpleDataBrief
{
	public String schemeName;
	public String id;
	public String name;
	public String version;

	public SimpleDataBrief(String schemename, String id, String name, String version)
	{
		this.schemeName = schemename;
		this.id = id;
		this.name = name;
		this.version = version;
	}

	public String getSchemeName()
	{
		return schemeName;
	}

	public String getId()
	{
		return id;
	}

	public String getName()
	{
		return name;
	}

	public String getVersion()
	{
		return version;
	}

}
